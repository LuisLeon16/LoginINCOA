genero CHAR(1) NOT NULL,
especialidad VARCHAR(20) NOT NULL,
año INT NOT NULL,
telefono INT NOT NULL,
cod_seccion INT FOREIGN KEY REFERENCES Secciones
);

-- -> TABLA ASIGNATURAS PROYECTO ESCUELA
GO
CREATE TABLE [dbo].[Asignaturas](
