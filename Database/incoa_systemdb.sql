-- -> TABLA DETALLES ASIGNATURAS PROYECTO ESCUELA
GO
CREATE TABLE [dbo].[AsignaturasAlumnos](
Id_AsAlum INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
cod_asignatura NVARCHAR(5) CONSTRAINT FK_cod_asignatura FOREIGN KEY(cod_asignatura)
REFERENCES [dbo].[Asignaturas] (cod_asignatura)
ON UPDATE CASCADE
ON DELETE CASCADE,
n_asignatura VARCHAR(50) NOT NULL,
cod_docente INT CONSTRAINT FK_cod_docente FOREIGN KEY(cod_docente)
REFERENCES [dbo].[Docentes] (cod_docente)
