-- -> PROCEDIMIENTO ALMACENADO
create procedure resumen
as
begin

	select a.cod_alumno as 'Codigo Alumno',a.cod_asignatura as 'Codigo Asignatura',
	b.promedio1 as 'Promedio 1', c.promedio2 as 'Promedio 2', d.promedio3 as 'Promedio 3',
	a.NotaFinal as 'Nota final' 
	from Notas a inner join Trimestre1 b on a.cod_alumno=b.cod_alumno and a.cod_asignatura=b.cod_asignatura
	inner join Trimestre2 c on a.cod_alumno=c.cod_alumno and a.cod_asignatura=c.cod_asignatura
	inner join Trimestre3 d on  a.cod_alumno=d.cod_alumno and a.cod_asignatura=d.cod_asignatura

end

exec resumen



