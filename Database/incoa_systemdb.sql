REFERENCES [dbo].[Alumnos] (cod_alumno)
ON UPDATE CASCADE
ON DELETE CASCADE,
);

-- -> TABLA NOTAS PROYECTO ESCUELA
GO
CREATE TABLE [dbo].[Notas](
cod_nota INT IDENTITY (1,1) PRIMARY KEY NOT NULL,
cod_alumno NVARCHAR(4) CONSTRAINT FK_cod_alumno2 FOREIGN KEY(cod_alumno)
REFERENCES [dbo].[Alumnos] (cod_alumno) 
ON UPDATE CASCADE
ON DELETE CASCADE,
cod_asignatura NVARCHAR(5) CONSTRAINT FK_cod_asignatura2 FOREIGN KEY(cod_asignatura)
REFERENCES [dbo].[Asignaturas] (cod_asignatura)
ON UPDATE CASCADE
ON DELETE CASCADE,
NotaFinal decimal(4,2)
);

-- -> TABLA TRIMESTRE 1 PROYECTO ESCUELA
